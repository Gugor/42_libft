int	ft_isalnum(char c)
{
	unsigned char	ch;

	ch = (unsigned char)c;
	return ((ch >= 'a' && ch <= 'z') || (ch >= 'A' && ch <= 'Z')
		|| (ch >= '0' && ch <= '9'));
}
